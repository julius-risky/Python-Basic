import math

class hitung(object):
    def __init__(self,p,l,t):
        pass

    @staticmethod
    def log10(x):
        return math.log10(x)

    @staticmethod
    def log(x):
        return math.log(x)
    
    @staticmethod
    def kali(x,y):
        return x * y

    @staticmethod
    def pangkat(x,y):
        return math.pow(x,y)
    
    @staticmethod
    def akar(x):
        return math.sqrt(x)
    
    @staticmethod
    def mutlak(x):
        return abs(x)

